class Ninja {
    constructor(ninjaName, ninjaHealth){
        this.name = ninjaName
        this.health = ninjaHealth
        this.strength = 3
        this.speed = 3
    }
    sayName() {
        this.name;
    }
    showStats() {
        this.name;
        this.health;
        this.strength;
        this.speed;
    }
    drinkSake() {
        this.health += 10;
    }
}

